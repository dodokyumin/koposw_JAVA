package Java_02;

public class ArithmeticOperators {

	public static void main(String[] args) {
		int a = 5;
		int b = 3;
		System.out.println(a + b);
		System.out.println(a - b);
		System.out.println(a * b);
		System.out.println(a / b);
		System.out.println(a % b);
		System.out.println(a++);
		System.out.println(b--);
	}

}
