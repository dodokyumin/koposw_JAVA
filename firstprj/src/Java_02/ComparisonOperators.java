package Java_02;

public class ComparisonOperators {

	public static void main(String[] args) {
		int qq  = 3;
		int ww = 3;
		int z = 5;
		
		System.out.println(qq == ww);
		System.out.println(qq != ww);
		System.out.println(qq > ww);
		System.out.println(qq < ww);
		System.out.println(qq < z);
		System.out.println(qq >= z);
		System.out.println(ww >= z);
	
	}

}
