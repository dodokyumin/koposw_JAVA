package Java_02;

public class AssignmentOperators {

	public static void main(String[] args) {
		int x = 7;
		System.out.println(x);

		x += 3;
		System.out.println(x);
		x -= 3;
		System.out.println(x);
		x *= 3;
		System.out.println(x);
		x /= 3;
		System.out.println(x);
		x %= 3;
		System.out.println(x);
		x = 10;
		System.out.println(x);
		x &= 3;
		System.out.println(x);
		x |= 3;
		System.out.println(x);
		x = 10;
		System.out.println(x);
		x ^= 3;
		System.out.println(x);
		x >>= 3;
		System.out.println(x);
		x <<= 3;
		System.out.println(x);

	}

}
