package p10;

public class k44_Calc1 {
	
	public int k44_sum(int k44_a, int k44_b) { //정수 2개 a와 b를 파라미터로 받아
		return k44_a + k44_b;	//그 두개를 더한 값을 리턴하는 함수
	}
	
	public int k44_sum(int k44_a, int k44_b, int k44_c) {//정수 3개 a와 b와 c를 파라미터로 받아
		return k44_a + k44_b + k44_c;	//그 세개를 더한 값을 리턴하는 함수
	}
	
	public int k44_sum(int k44_a, int k44_b, int k44_c, int k44_d) {//정수 4개 a와 b와 c와 d를 파라미터로 받아
		return k44_a + k44_b + k44_c + k44_d;	//그 네개를 더한 값을 리턴하는 함수
	}
	
	public double k44_sum(double k44_a, double k44_b) {//실수 2개 a와 b를 파라미터로 받아
		return k44_a + k44_b;	//그 실수 둘을 더한 값을 리턴해주는 함수
	}
	
	
	
}